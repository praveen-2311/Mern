// Function Declaration
function greet(name) {
    return `Hello, ${name}!`;
}
console.log(greet("Praveen"));

// Function Expression
const add = function(x, y) {
    return x + y;
};
console.log(add(5, 10));

// Arrow Function
const multiply = (a, b) => a * b;
console.log(multiply(2, 3));
